// Cria uma instância do objeto Date
// Representa um único momento no tempo
// Baseado nos milissegundos desde 1º de janeiro de 1970 (UTC)
const momento = new Date();
// Exibe a data e hora atual
console.log("Data e hora atuais:", momento);
// Exibe o valor do tempo em milissegundos desde 1970
console.log("Milissegundos desde 01/01/1970:", momento.getTime());
